#include "delay.h"

/*******************************************************************************
* 函 数 名         : Delay_ms
* 函数功能		   : 延时函数，1Ms的延时
*******************************************************************************/
void Delay_ms(U16 z)
{
	U16 x,y;

	for(x=z;x>0;x--)

		for(y=110;y>0;y--);
}


/*******************************************************************************
* 函 数 名         : Delay_10us
* 函数功能		   : 延时函数，us的延时
*******************************************************************************/
void Delay_us(U16 n)
{
	while(n--);
}
#include "../src/printf_init/printf_init.h"

/*******************************************************************************
* 函 数 名         : UartPrintInit											  
* 函数功能		   : 使用后可以使用printf进行串口输出调试
*******************************************************************************/ 
void UartPrintInit(void)
{
	TMOD |= (1<<5);		   //T1方式2，T0方式1
	SCON |= 0x50;	

	TH1=0xFD;		  //0xFD为设置9600为串口波特率
	TL1=0xFD;
	TR1=1;			   //开启定时器T1
	TI=1;
	
	EA=1;			   //开启总中断
}
#include "delay.h"

/*******************************************************************************
* 函 数 名         : Delay_us
* 函数功能		   : 延时函数，i=1时，大约延时10us
*******************************************************************************/
void Delay_us(U16 i)
{
    while(i--); 
}
/**********************************************************
函数：delay_pinglu
功能：控制频率延时 
*************************************************************/ 

void delay_pinglu (U16 n)   //控制频率延时    
{   
 unsigned i;
 i=3*n;   
 while(--i);   
}
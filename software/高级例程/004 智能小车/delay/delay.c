/*******************************************************************************
* 函 数 名         : delay_ms
* 函数功能		   : 延时函数，i=1时，大约延时1ms
*******************************************************************************/
void delay_ms(unsigned int n)
{
	unsigned int  i,j;
	for(i=0;i<n;i++)
		for(j=0;j<123;j++);
}	
/*******************************************************************************
* 函 数 名         : nop
* 函数功能		   : 延时函数，当晶振为12HZ，i=1时，大约延时2us
*******************************************************************************/
void nop(unsigned int n)
{	  
	unsigned int i;
	for(i=0;i<n;i++)
	{
	   _nop_();
	}
}

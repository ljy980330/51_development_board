/*******************************************************************************
* 函 数 名         : oled_IIC_Start
* 函数功能		   : 开始
*******************************************************************************/
 void oled_IIC_Start()
{
   oled_SCL = 1;		
   oled_SDA = 1;
   oled_SDA = 0;
   oled_SCL = 0;
}								   
/*******************************************************************************
* 函 数 名         : oled_IIC_Stop
* 函数功能		   : 停止
*******************************************************************************/
void oled_IIC_Stop()
{
   oled_SCL = 0;
   oled_SDA = 0;
   oled_SCL = 1;
   oled_SDA = 1;
}	
/*******************************************************************************
* 函 数 名         : oled_Write_IIC_Byte
* 函数功能		   : 写字节
*******************************************************************************/
void oled_Write_IIC_Byte(uchar IIC_Byte)
{
	uchar i;
	for(i=0;i<8;i++)
	{
		if(IIC_Byte & 0x80)
			oled_SDA=1;
		else
		oled_SDA=0;
		oled_SCL=1;
		oled_SCL=0;
		IIC_Byte<<=1;
	}
	oled_SDA=1;
	oled_SCL=1;
	oled_SCL=0;
} 			   
/*******************************************************************************
* 函 数 名         : OLED_12864_Write_data(
* 函数功能		   : 写入数
*******************************************************************************/
void OLED_12864_Write_data(uint dat)
{
	oled_IIC_Start();
	oled_Write_IIC_Byte(0x78);
	oled_Write_IIC_Byte(0x40);
	oled_Write_IIC_Byte(dat);
	oled_IIC_Stop();
} 
/*******************************************************************************
* 函 数 名         : OLED_12864_Write_commomand(
* 函数功能		   : 写入命令
*******************************************************************************/
void OLED_12864_Write_commomand(uint dat)
{
	oled_IIC_Start();
	oled_Write_IIC_Byte(0x78);
	oled_Write_IIC_Byte(0x00);
	oled_Write_IIC_Byte(dat);
	oled_IIC_Stop();
} 
/*******************************************************************************
* 函 数 名         : OLED_SetPos
* 函数功能		   : 设置起始坐标 
*******************************************************************************/
void OLED_SetPos(uchar x, uchar y)
{ 
	OLED_12864_Write_commomand(0xb0+y);
	OLED_12864_Write_commomand(((x&0xf0)>>4)|0x10);
	OLED_12864_Write_commomand((x&0x0f)|0x01);
}  
/*******************************************************************************
* 函 数 名         : OLED_Fill
* 函数功能		   : 屏幕填充 
*******************************************************************************/
void OLED_Fill(uchar bmp_dat)//
{
	uchar y,x;
	for(y=0;y<8;y++)
	{
		OLED_12864_Write_commomand(0xb0+y);
		OLED_12864_Write_commomand(0x01);
		OLED_12864_Write_commomand(0x10);
		for(x=0;x<128;x++)
		OLED_12864_Write_data(bmp_dat);
	}
} 
	 
/*******************************************************************************
* 函 数 名         : OLED_8x16Str
* 函数功能		   : 显示8*16的坐标(x,y),y为页范围0～7
*******************************************************************************/
void OLED_8x16Str(uchar x, uchar y, uchar ch)
{
	uchar i=0;
	if(x>120)
	{
		x=0;y++;
	}

	OLED_SetPos(x,y);
	for(i=0;i<8;i++)
	{
		OLED_12864_Write_data(F8X16[ch*16+i]);
	}

	OLED_SetPos(x,y+1);
	for(i=0;i<8;i++)
	{
		OLED_12864_Write_data(F8X16[ch*16+i+8]);
	}
}	
/*******************************************************************************
* 函 数 名         : OLED_16x16CN
* 函数功能		   : 显示16*16的坐标(x,y)
*******************************************************************************/
void OLED_16x16CN(uchar x,uchar y,uchar N)
{
	uchar i=0;
	uint adder=32*N;

	OLED_SetPos(x,y);
	for(i=0;i<16;i++)
	{
		OLED_12864_Write_data(F16x16[adder]);
		adder+=1;
	}

	OLED_SetPos(x,y+1);
	for(i=0;i<16;i++)
	{
		OLED_12864_Write_data(F16x16[adder]);
		adder+=1;
	}
}  
